-- Уведомления заявителям о смене статуса заявок (канал — только e-mail; решение заказчика 2026-06-24).

CREATE TABLE applicant_notifications (
    id BIGSERIAL PRIMARY KEY,
    recipient_email VARCHAR(255),
    recipient_name VARCHAR(255),
    service_type VARCHAR(120),
    app_no VARCHAR(50),
    status VARCHAR(80),
    message TEXT,
    channel VARCHAR(20) NOT NULL DEFAULT 'email',
    is_read BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_applicant_notif_email ON applicant_notifications(recipient_email);

